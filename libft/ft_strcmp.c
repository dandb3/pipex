#include "libft.h"

int	ft_strcmp(const char *str1, const char *str2)
{
	while (*str1 && *str2 && *str1 == *str2)
	{
		str1++;
		str2++;
	}
	return ((int)(unsigned char)(*str1) - (int)(unsigned char)(*str2));
}
